package model;


public class MIva {
	
	private int Id;
	private String iva;
	
	public int getId() {
		return Id;
	}
	public void setId(int id) {
		Id = id;
	}
	public String getIva() {
		return iva;
	}
	public void setIva(String iva) {
		this.iva = iva;
	}
	
	public String toString(String iva) {
		return iva;
	}


}
